# string methods

str="welcome to the world of artificial intelligence"
print(str.capitalize()) # Capitalize the first letter
print(str.find("world")) # Find the position of the word "world"
print(str.replace("artificial", "machine")) # Replace "artificial" with "machine"
print(str.encode("utf-8")) # Encode the string to bytes
print(str.count("i")) # Count the occurrences of the letter "i"